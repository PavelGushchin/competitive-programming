/*
    Будем красить по кругу. Введём начальное положение "кисть за пределами квадрата"
    Надо пройти a вниз, затем a-b вправо и вызваться рекурсивно от квадрата (a-b) x (a-b)
    Для рекурсивной подзадачи будет такое же положение, кисть за пределами квадрата
    
    Пример
    
    1. идём вниз "a":
    @@
    @@
    .....
    .....
    .....
    .....
    .....
    
    2. Идём вправо "a-b":
    ##...
    ##...
    ##...
    @@...
    @@...
    
    3. Вызываемся рекурсивно:
    ##...
    ##...
    ##...
    ###@@
    ###@@
*/
#include <bits/stdc++.h>
using namespace std;
int64_t solve(int64_t a, int64_t b) {
    if (a <= b)
        return a;
    return a + a - b + solve(a-b, b);
}
main() {
    int64_t a, b;
    cin >> a >> b;
    cout << solve(a,b)-b;
}